def sum_num(num1, num2):
    return num1 + num2


def subtract_num(num1, num2):
    return num1 - num2


def divide_num(num1, num2):
    return num1 // num2


def number_is_even_or_odd(num):
    if num % 2 == 0:
        return 'numero par'
    elif num % 2 != 0:
        return 'numero impar'


def divide_number(num1, num2):
    return num1 / num2